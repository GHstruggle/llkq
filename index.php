            beforeSend: function() {}, //请求前
            complete: function() {}, //请求完后调用，，，请求成功失败都会调用
            success: function(res) {
                let listWarp = document.getElementById('list-warp');
                if (!res.data) {
                    loadingMSG({
                        wrap: 'list-warp',
                        message: '数据为空'
                    })
                    return false
                }
                // console.log(res)
                let ul = `<ul class="clearfix">`;
                res.data.forEach(item => {
                    ul += `<a href="newsDetail.php?id=${item.id}"><li>
                        <img src="${item.imgUrl}" alt="${item.title}">
                        <h5>${item.title}</h5>
                        <p>${item.dec}</p>
                    </li>
                    </a>
                    `
                })
                ul += '</ul>';
                listWarp.innerHTML = ul;

            },
            error: function() {} //请求失败时被调用的函数
        })


        // swiper 初始化
        var swiper = new Swiper('.swiper-container', {
